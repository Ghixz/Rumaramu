     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'nama' => 'required',
        ],[
            'nama.required' => 'Nama Tidak Boleh Kosong',
        ]);

        Contact::where('id', $id)
            ->update([
            'nama' => $request->nama

